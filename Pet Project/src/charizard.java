public class Charizard {
private  int health;
private  int energy;
private  int level;
private  int temperature;

    public Charizard() {
        this.energy = 100;
        this.health = 100;
        this.level = 1;
        this.temperature = 36;
}



public int getHealth() {

    return health;

}



public int getEnergy() {

    return energy;

}



public int getLevel() {

    return level;

}



public double getTemperature() {

    return temperature;

}

    

}
